a=float(input("enter the first number"))
b=float(input("enter the second number"))
operation=input("enter operation +,-,*,/")
if operation=="+":
 print("result",a+b)
elif operation=="-":
 print("result",a-b)
elif operation=="*":
 print("result",a*b)
elif operation=="/":
 print("result",a/b)
else:
 print("invalid operation")
